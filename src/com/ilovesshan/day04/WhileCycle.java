public class WhileCycle {
    public static void main(String[] args) {
        // while(){}
        int i1 = 0;
        while (i1 < 10) {
            i1++;
            System.out.println("好好学习~");
        }

        // do{} while()
        int i2 = 0;
        do {
            i2++;
            System.out.println("好好学习~");
        } while (i2 < 10);


    }
}
